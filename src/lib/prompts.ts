export function buildAnalysisPrompt(transcription: string): string {
  return `あなたは医療分野の専門家です。以下の文字起こしテキストを分析してください。

## 指示
1. テキストから「その日に学んだこと」を箇条書きで抽出してください。具体的な医学知識や臨床上の気づきを中心に抽出してください。
2. テキストから「エンドユーザーが課題と感じていること」を箇条書きで抽出してください。患者や医療従事者が困っている点、改善が必要な点を抽出してください。
3. テキスト中の医学用語で、音声認識により誤って文字起こしされた可能性のある単語を特定し、正しい医学用語の候補を複数提示してください。明らかに正しく文字起こしされている場合は空配列としてください。

## 文字起こしテキスト
${transcription}

## 出力形式
以下のJSON形式で出力してください。JSON以外のテキストは含めないでください。
{
  "learnings": ["学んだこと1", "学んだこと2"],
  "challenges": ["課題1", "課題2"],
  "medicalTerms": [
    {
      "original": "誤認識された単語",
      "candidates": ["正しい候補1", "正しい候補2", "正しい候補3"]
    }
  ]
}`;
}
